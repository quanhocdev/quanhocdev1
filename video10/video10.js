// Vòng lặp for
for (let i = 0; i < 3; i++) {
  console.log(i);
}
// Vòng lặp while
let a = 4;
while (a < 10) {
  console.log("Hi");
  a++;
}
//Vòng lặp do...while
let b = 8;
do {
  console.log("love");
  b++;
} while (b < 10);
// loop với arr
let arr = ["Tai", "Quan", "Hoan", "Hon"];
for (let i = 0; i < arr.length; i++) {
  console.log(arr[i]);
}
